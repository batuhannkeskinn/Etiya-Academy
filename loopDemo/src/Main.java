public class Main {
    public static void main(String[] args) {
        //For
        for(int i = 1 ; i<=10 ; i++){
            System.out.println(i);
        }
        System.out.println("for döngüsü sona erdi");
        int i =1;
        while (i<10){
        System.out.println(i);
        i+=1;
        }
        System.out.println("while döngüsü sona erdi!");
    }
}